# 수학적인 기능을 포함하고 있는 라이브러리

# 1. 팩토리얼
import math

print(math.factorial(5))  # 5! / 120

# 2. 제곱근
import math
print(math.sqrt(7)) # 7의 제곱근 / 2.6457513110645907

# 3. 최대 공약수
import math
print(math.gcd(21, 14)) # 7

# 4. pi
import math
